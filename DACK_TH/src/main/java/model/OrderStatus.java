package model;

import java.io.Serializable;

/**
 *
 * @author phuongkhanh
 */
public class OrderStatus implements Serializable{
    private int id;
    private String status;

    public OrderStatus() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
    
    
}
